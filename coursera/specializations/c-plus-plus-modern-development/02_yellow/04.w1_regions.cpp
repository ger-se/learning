#include <iostream>
#include <string>
#include <vector>
#include <map>

using namespace std;

enum class Lang {
  DE, FR, IT
};

struct Region {
  string std_name;
  string parent_std_name;
  map<Lang, string> names;
  int64_t population;
};

bool operator<(const Region& lhs, const Region& rhs){
    auto l = make_tuple(lhs.std_name, lhs.parent_std_name,lhs.population, lhs.names);
    auto r = make_tuple(rhs.std_name, rhs.parent_std_name,rhs.population, rhs.names);
    return l < r;
}

int FindMaxRepetitionCount(const vector<Region>& regions){
    map<Region,int> mapped;
    int maximum = 0;
    for(auto& region: regions){
        auto current = mapped[region];
        current += 1;
        mapped[region] = current;
        if(maximum < current){
            maximum = current;
        }
    }

    return maximum;
}

